word = input('Введете строку: ')

for lettet in word:
    print("{}.".format(lettet), end='')

print('\n')

for i in range(10):
    print(i, end=' ')

print('\n')

for i in range(5, 10):
    print(i, end=' ')

print('\n')

for i in range(20, 10, -1):
    print(i, end=' ')

print('\n')

for i in range(5, 30, 3):
    print(i, end=' ')

print('\n')
